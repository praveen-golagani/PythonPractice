for i in range(10):
    print(i)            # 0..9

for i in range(1,11):   # 1..10
    print(i)

# even b/w 1...20
for i in range(2,21,2):
    print(i)

# odd b/w 1..20
for i in range(1,21,2):
    print(i)

# print 1 to 10 in desc order
for i in range(10,0,-1):
    print(i)

# print 5, 10, 15, ...100
for i in range(5,101,5):
    print(i)